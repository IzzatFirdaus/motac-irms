# Dasar Privasi untuk Sistem Pengurusan Sumber Bersepadu MOTAC

**Kemaskini Terakhir:** 7 Ogos 2025

---

## 1. Pengenalan

Dasar Privasi ini menerangkan bagaimana Bahagian Pengurusan Maklumat (BPM) di Kementerian Pelancongan, Seni dan Budaya Malaysia (MOTAC) ("kami") mengumpul, menggunakan, mendedahkan, dan melindungi maklumat peribadi anda apabila anda menggunakan Sistem Pengurusan Sumber Bersepadu MOTAC ("Sistem"). Sistem ini memudahkan permohonan Email/ID Pengguna dan Pinjaman Peralatan ICT.

Dengan menggunakan Sistem ini, anda bersetuju dengan pengumpulan dan penggunaan maklumat sebagaimana yang dinyatakan dalam dasar ini.

---

## 2. Maklumat Yang Kami Kumpul

Kami mengumpul pelbagai jenis maklumat peribadi dan berkaitan permohonan untuk mengendalikan serta memperbaiki Sistem kami.

### 2.1 Data Peribadi

Kami mungkin meminta maklumat yang boleh mengenal pasti anda secara peribadi, termasuk tetapi tidak terhad kepada:

- Nama penuh dan gelaran (cth: "Encik", "Puan")
- Nombor pengenalan (NRIC/MyKad) atau nombor pasport
- Alamat emel rasmi MOTAC dan peribadi
- ID Pengguna yang diberikan (cth: ID rangkaian)
- Jawatan, gred, jabatan, dan aras
- Nombor telefon bimbit
- Status perkhidmatan (cth: Tetap, Kontrak, MySTEP), dan jenis lantikan (cth: Baharu, Kenaikan Pangkat/Pertukaran)
- Maklumat jabatan/emel terdahulu (jika berkenaan)
- Foto profil (pilihan)
- Kata laluan log masuk sistem (disulitkan)

### 2.2 Data Permohonan

Maklumat berkaitan permohonan anda, seperti:

**Permohonan Email/ID Pengguna**
- ID emel yang dicadangkan, butiran emel kumpulan
- Tarikh mula/tamat perkhidmatan
- Tujuan/catatan permohonan
- Maklumat pegawai sokongan
- Pengesahan kebenaran maklumat dan persetujuan penggunaan data secara bertanggungjawab

**Permohonan Pinjaman Peralatan ICT**
- Tujuan, lokasi, dan lokasi pemulangan peralatan
- Tarikh pinjaman dan pemulangan
- Maklumat pegawai bertanggungjawab (jika berbeza)
- Jenis dan kuantiti peralatan yang dimohon
- Pengesahan terma & syarat
- Maklumat pegawai sokongan

### 2.3 Data Penggunaan & Transaksi Sistem

- Rekod kelulusan (maklumat pegawai, status, ulasan, cap masa)
- Transaksi pinjaman peralatan ICT (rekod pengeluaran/pemulangan, senarai semak, nota)
- Jejak audit (created_by, updated_by, rekod notifikasi)

---

## 3. Cara Kami Menggunakan Maklumat Anda

Data anda digunakan untuk:

- Menyediakan, mengoperasi dan menyelenggara Sistem
- Mengurus akaun pengguna dan akses berasaskan peranan
- Memproses permohonan Email/ID Pengguna dan Pinjaman Peralatan ICT
- Memudahkan kelulusan aliran kerja dan notifikasi
- Menjejak inventori peralatan ICT dan status pinjaman
- Mentadbir dan melindungi Sistem, mengekalkan jejak audit
- Memastikan pematuhan kepada polisi MOTAC
- Memantau prestasi dan mengumpul maklum balas untuk penambahbaikan

---

## 4. Perkongsian & Pendedahan Data

Maklumat anda mungkin dikongsi secara dalaman dalam MOTAC dengan pegawai yang memerlukan akses bagi tujuan yang sah, termasuk tetapi tidak terhad kepada:

- Pegawai Sokongan (semakan permohonan)
- Staf BPM (pinjaman peralatan ICT)
- Pentadbir IT (peruntukan emel/ID pengguna, sokongan)
- Pegawai lain yang dibenarkan (kelulusan, pengurusan)

Kami **tidak** berkongsi data peribadi anda dengan pihak ketiga luar untuk tujuan pemasaran. Pendedahan hanya berlaku jika dikehendaki oleh undang-undang atau untuk:

- Memenuhi kewajipan undang-undang
- Melindungi hak atau harta MOTAC
- Mencegah atau menyiasat salah laku
- Melindungi keselamatan pengguna/orang awam

*Nota: Semasa pembangunan, ujian emel mungkin menggunakan perkhidmatan seperti Mailtrap. Namun, ini **tidak** terpakai untuk emel produksi sebenar yang mengandungi data sensitif.*

---

## 5. Keselamatan Data

Kami menggunakan langkah keselamatan yang mantap, termasuk:

- Ciri keselamatan dan pengesahan Laravel
- Kawalan akses berasaskan peranan (RBAC)
- Perlindungan terhadap kelemahan web (CSRF, sanitasi input)
- Pengurusan kata laluan dan sesi yang selamat
- Pengesahan tandatangan webhook tersuai

Tiada kaedah penghantaran atau penyimpanan data atas talian yang 100% selamat, namun kami berusaha melindungi maklumat anda sebaik mungkin.

---

## 6. Tempoh Penyimpanan Data

Kami hanya menyimpan data peribadi dan permohonan selama tempoh yang diperlukan untuk tujuan yang dinyatakan, sepanjang tempoh perkhidmatan anda dengan MOTAC, dan mengikut keperluan undang-undang Malaysia.

---

## 7. Hak Anda

Anda boleh mengakses dan mengemas kini maklumat peribadi di profil pengguna anda, tertakluk kepada keupayaan sistem. Anda boleh melihat status dan butiran permohonan anda. Hubungi BPM jika anda perlukan bantuan atau pembetulan data.

