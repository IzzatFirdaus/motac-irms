## 5. Terma Pinjaman Peralatan ICT

- **Kelayakan:** Hanya staf MOTAC yang diluluskan dan aktif boleh memohon pinjaman peralatan ICT.
- **Ketersediaan:** Peralatan dipinjam berdasarkan siapa cepat dia dapat, tertakluk kepada ketersediaan.
- **Masa Pemprosesan:** Permohonan lengkap akan diproses oleh BPM dalam tempoh tiga (3) hari bekerja. BPM tidak bertanggungjawab sekiranya peralatan tidak tersedia akibat kelewatan atau ketidaklengkapan permohonan.
- **Tanggungjawab:** Pemohon atau pegawai bertanggungjawab sepenuhnya terhadap sebarang kehilangan atau kerosakan peralatan semasa tempoh pinjaman.
- **Pengesahan:** Dengan menyerahkan permohonan Pinjaman Peralatan ICT, anda mengesahkan penerimaan terma pinjaman.
- **Pegawai Sokongan:** Kelulusan oleh pegawai sokongan dengan gred minimum (cth: Gred 41 ke atas) diperlukan.

