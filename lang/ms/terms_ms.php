## 4. Terma Permohonan Email/ID Pengguna

- **Kelayakan:** Akaun emel MOTAC sepenuhnya biasanya tersedia kepada Staf Tetap, Staf Kontrak dan MySTEP. ID Pengguna (tanpa penciptaan peti emel MOTAC baru) boleh diberikan kepada pelajar latihan industri (hanya di HQ) atau staf yang menggunakan peti emel agensi sedia ada yang memerlukan komunikasi rasmi domain motac.gov.my.
- **Pengesahan:** Dengan menghantar permohonan Email/ID Pengguna, anda mengesahkan bahawa:
    1. Semua maklumat yang diberikan adalah benar.
    2. BPM boleh menggunakan maklumat ini untuk tujuan pemprosesan.
    3. Anda bertanggungjawab sepenuhnya ke atas setiap emel yang dihantar dan diterima melalui akaun emel MOTAC anda.
- **Pegawai Sokongan:** Permohonan perlu disemak oleh pegawai sokongan yang memenuhi syarat gred minimum (cth: Gred 9 ke atas untuk permohonan emel).

