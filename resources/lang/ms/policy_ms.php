# Dasar Privasi untuk Sistem Pengurusan Sumber Bersepadu MOTAC

**Kemaskini Terakhir:** 7 Ogos 2025

---

## 1. Pengenalan

Dasar Privasi ini menerangkan bagaimana Bahagian Pengurusan Maklumat (BPM) di Kementerian Pelancongan, Seni dan Budaya Malaysia (MOTAC) ("kami") mengumpul, menggunakan, mendedahkan, dan melindungi maklumat peribadi anda apabila anda menggunakan Sistem Pengurusan Sumber Bersepadu MOTAC ("Sistem"). Sistem ini memudahkan permohonan Pinjaman Peralatan ICT dan sokongan helpdesk dalaman untuk staf rasmi MOTAC.

Dengan menggunakan Sistem ini, anda bersetuju dengan pengumpulan dan penggunaan maklumat sebagaimana yang dinyatakan dalam dasar ini.

---

## 2. Maklumat Yang Kami Kumpul

Kami mengumpul pelbagai jenis maklumat peribadi dan berkaitan permohonan untuk mengendalikan serta memperbaiki Sistem kami.

### 2.1 Data Peribadi

Kami mungkin meminta maklumat yang boleh mengenal pasti anda secara peribadi, termasuk tetapi tidak terhad kepada:

- Nama penuh dan gelaran (cth: "Encik", "Puan")
- Nombor pengenalan (NRIC/MyKad) atau nombor pasport
- Alamat emel rasmi MOTAC dan peribadi
- ID Pengguna yang diberikan (cth: ID rangkaian)
- Jawatan, gred, jabatan, dan aras
- Nombor telefon bimbit
- Status perkhidmatan (cth: Tetap, Kontrak, MySTEP), dan jenis lantikan (cth: Baharu, Kenaikan Pangkat/Pertukaran)
- Maklumat jabatan/emel terdahulu (jika berkenaan)
- Foto profil (pilihan)
- Kata laluan log masuk sistem (disulitkan)

### 2.2 Data Permohonan

Maklumat berkaitan permohonan anda, seperti:

- Permohonan Pinjaman Peralatan ICT: Tujuan, lokasi, lokasi pemulangan, tarikh pinjaman & pemulangan, maklumat pegawai bertanggungjawab, jenis & kuantiti peralatan, pengesahan terma & syarat, maklumat pegawai sokongan.
- Permintaan Helpdesk: Kandungan tiket, komunikasi, lampiran, status dan tindakan aliran kerja.

### 2.3 Data Penggunaan & Transaksi Sistem

- Rekod kelulusan (maklumat pegawai, status, ulasan, cap masa)
- Transaksi pinjaman peralatan ICT (rekod pengeluaran/pemulangan, senarai semak, nota)
- Interaksi helpdesk (aktiviti, respons, penutupan)
- Jejak audit (created_by, updated_by, rekod notifikasi)

---

## 3. Cara Kami Menggunakan Maklumat Anda

Data anda digunakan untuk:

- Menyediakan, mengoperasi dan menyelenggara Sistem
- Mengurus akaun pengguna dan akses berasaskan peranan
- Memproses permohonan Pinjaman Peralatan ICT dan permintaan helpdesk
- Memudahkan kelulusan aliran kerja dan notifikasi
- Menjejak inventori peralatan ICT dan status pinjaman
- Mentadbir dan melindungi Sistem, mengekalkan jejak audit
- Memastikan pematuhan kepada polisi MOTAC dan undang-undang Malaysia
- Memantau prestasi dan mengumpul maklum balas untuk penambahbaikan

---

## 4. Perkongsian & Pendedahan Data

Maklumat anda mungkin dikongsi secara dalaman dalam MOTAC dengan pegawai yang dibenarkan bagi tujuan yang sah, termasuk staf BPM, pentadbir IT, dan pengurusan.

- Data peribadi anda **tidak** dikongsi dengan pihak ketiga luar untuk tujuan pemasaran.
- Pendedahan hanya berlaku jika dikehendaki oleh undang-undang atau untuk:
    - Memenuhi kewajipan undang-undang
    - Melindungi hak atau harta MOTAC
    - Mencegah atau menyiasat salah laku
    - Melindungi keselamatan pengguna/orang awam

Semua permintaan dan komunikasi helpdesk direkod dan dilindungi menurut dasar ini.

---

## 5. Keselamatan Data

Kami menggunakan langkah keselamatan yang mantap, termasuk:

- Ciri keselamatan dan pengesahan Laravel
- Kawalan akses berasaskan peranan (RBAC)
- Perlindungan terhadap kelemahan web (CSRF, sanitasi input)
- Pengurusan kata laluan dan sesi yang selamat
- Pengesahan tandatangan webhook tersuai

Audit dan pemantauan berkala dijalankan. Tiada kaedah penghantaran atau penyimpanan data atas talian yang 100% selamat, namun kami berusaha melindungi maklumat anda sebaik mungkin.

---

## 6. Pemaksimuman Data & Had Tujuan

Kami hanya mengumpul dan memproses data minimum yang perlu untuk memenuhi tujuan rasmi MOTAC seperti yang dikehendaki undang-undang dan dasar dalaman.

---

## 7. Log Audit

Semua aktiviti pengguna dalam Sistem, termasuk helpdesk dan permohonan pinjaman, direkodkan untuk tujuan keselamatan, akauntabiliti, dan pematuhan. Log audit disimpan mengikut dasar MOTAC dan kerajaan.

---

## 8. Tempoh Penyimpanan Data

Data peribadi dan permohonan disimpan hanya selama tempoh yang diperlukan untuk tujuan yang dinyatakan, sepanjang tempoh perkhidmatan anda dengan MOTAC, dan mengikut keperluan undang-undang Malaysia. Permintaan untuk pemadaman akaun atau data boleh dikemukakan kepada BPM dan akan diproses mengikut prosedur rasmi.

---

## 9. Hak Anda

Anda boleh mengakses dan mengemas kini maklumat peribadi di profil pengguna anda, tertakluk kepada keupayaan sistem. Anda boleh memohon pembetulan data dengan menghubungi BPM. Permintaan akses, pembetulan atau pemadaman tertakluk kepada kelulusan mengikut keperluan rekod rasmi.

---

## 10. Pemberitahuan Pelanggaran Data

Sekiranya berlaku pelanggaran data atau insiden yang disyaki, BPM akan segera menyiasat dan memaklumkan pengguna yang terjejas mengikut peraturan kerajaan Malaysia dan prosedur dalaman MOTAC.

---

## 11. Perkhidmatan Pihak Ketiga

Jika perkhidmatan pemproses pihak ketiga (termasuk awan kerajaan atau SaaS) digunakan, ia tertakluk kepada tahap keselamatan dan kewajipan undang-undang yang setara. Data peribadi anda tidak dikongsi dengan pihak luar untuk pemasaran.

---

## 12. Pematuhan Undang-Undang

Dasar ini diwujudkan selaras dengan Akta Perlindungan Data Peribadi 2010 (PDPA), Akta Rahsia Rasmi 1972, dan Polisi Keselamatan ICT Sektor Awam Malaysia (MyMIS).

---

## 13. Penguatkuasaan

Staf MOTAC yang melanggar dasar ini tertakluk kepada tindakan tatatertib di bawah peraturan perkhidmatan awam dan undang-undang yang berkenaan.

